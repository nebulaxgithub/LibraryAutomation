    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String u_adi = ad.getText();
        String u_soyadi = soyad.getText();
        String u_kadi = kad.getText();
        Integer u_sifre = Integer.parseInt(pass.getText());
        Integer u_sifre2 = Integer.parseInt(pass2.getText());
        String yetki_sec = (String) yetki.getSelectedItem();
        
         try {
                rs.moveToInsertRow();
                if(u_adi.equals("")){
                goster.setText("Adinizi Giriniz...");
                }else if(u_soyadi.equals("")){
                goster.setText("Soyadinizi Girinizç...");
                }else if(u_kadi.equals("")){
                 goster.setText("Kullanici Adınizi Giriniz...");
                }else if(u_sifre.equals("")){
                goster.setText("Sifrenizi Giriniz");
                }else if(u_sifre != u_sifre2){
                goster.setText("Sifreler uyuşmuyor...");
                }else{
                
                rs.updateString("adi", u_adi);
                rs.updateString("soyadi", u_soyadi);
                rs.updateString("kadi",u_kadi);
                rs.updateInt("sifresi", u_sifre);
                rs.updateString("yetkisi",yetki_sec);
                

                rs.insertRow();
                goster.setText("Kayit yapildi..");
                }
            } catch (SQLException ex) {
                Logger.getLogger(UyeEkle.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jButton1ActionPerformed

